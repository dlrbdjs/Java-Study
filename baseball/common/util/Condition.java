package baseball.common.util;

public class Condition {
    public static boolean isSameNumber(int num1, int num2) {
        return num1 == num2;
    }

    public static boolean isZero(int num) {
        return num == 0;
    }

    public static boolean isThree(int num1) {
        return num1 == 3;
    }
}
